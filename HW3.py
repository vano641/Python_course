# Требуется вывести все целые степени двойки (т.е. числа вида 2k), не превосходящие числа N.
# n=16
# Вывод
# 1
# 2
# 4
# 8
# 16

# Мое Решение:

n = 1024
degree = 0
res = 0
while res <= n:
    res = 2 ** degree
    degree += 1
    if res <= n:
        print(res)

# Решение в Автотестах:
n = 1024
i = 0
while 2 ** i <= n:
    print(2 ** i)
    i += 1